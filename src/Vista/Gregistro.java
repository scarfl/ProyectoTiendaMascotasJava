private void validarFormatoContraseña(String contraseña) throws IllegalArgumentException {
    // Expresión regular para validar el formato de contraseña
    String patron = "^(?=.[a-z])(?=.[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{8,}$";
    
    if (!contraseña.matches(patron)) {
        throw new IllegalArgumentException("La contraseña no cumple con los requisitos mínimos");
    }

             
       
}      
         
 
         
         
