    // Método para validar la contraseña
private void validatePassword(String password) {
// Validar longitud mínima
        if (password.length() < 8) {
            throw new IllegalArgumentException("La contraseña debe tener al menos 8 caracteres");
        }
        // Validar que comience con una letra mayúscula
        if (!Character.isUpperCase(password.charAt(0))) {
            throw new IllegalArgumentException("La contraseña debe comenzar con una letra mayúscula");
        }

        // Validar que contenga al menos 1 dígito
        boolean hasDigit = false;
        for (char c : password.toCharArray()) {
            if (Character.isDigit(c)) {
                hasDigit = true;
                break;
            }
        }
        if (!hasDigit) {
            throw new IllegalArgumentException("La contraseña debe contener al menos 1 dígito");
        }

        // Validar que contenga al menos 1 carácter especial
        String specialChars = "~`!@#$%^&*()-_=+\\|[{]};:'\",<.>/?";
        boolean hasSpecialChar = false;
        for (char c : password.toCharArray()) {
            if (specialChars.indexOf(c) != -1) {
                hasSpecialChar = true;
                break;
            }
        }
        if (!hasSpecialChar) {
            throw new IllegalArgumentException("La contraseña debe contener al menos 1 carácter especial");
        }
    

        
    }                                        
private void validarFormatoCorreo(String correo) throws IllegalArgumentException {
    // Expresión regular para validar el formato de correo electrónico
    String patron = "^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";
    
    if (!correo.matches(patron)) {
        throw new IllegalArgumentException("El formato de correo electrónico es inválido");
    }

    }                                          

